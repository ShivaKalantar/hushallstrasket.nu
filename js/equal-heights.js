$(window).on("load resize", function(){
    /*-- Set Height ---*/
    var heights = $(".A").map(function ()
        {
            return $(this).height();
        }).get(),

        maxHeight = Math.max.apply(null, heights)+'px';
    if($('#visMd').is(":visible")) {
        setTimeout(function () {
            $(".A").height(maxHeight);
        }, 300);
    }else{
        $(".A").css({'height':'auto'});
    }
});
